public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("HelloWorld, world!");
        System.out.println(2 + 2);
        System.out.println(2 - 2);
        System.out.println(2 / 2);
        System.out.println(2 - 2);

        System.out.println(1 / 2);
        System.out.println(1.0 / 2);

        System.out.println("2" + "2");
        System.out.println("2" + 2);
        System.out.println("2 + 2 = " + (2 + 2));
    }
}
